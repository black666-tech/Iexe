export enum AppStep {
  WELCOME,
  METADATA,
  CONFIGURATION,
  CODE_INPUT,
  REVIEW,
  GENERATION
}

export enum TargetArch {
  X64 = 'x64',
  X86 = 'x86',
  ANYCPU = 'anycpu'
}

export enum OutputType {
  CONSOLE = 'exe',
  WINDOWED = 'winexe'
}

export interface ExeMetadata {
  projectName: string;
  version: string;
  author: string;
  description: string;
  copyright: string;
}

export interface ExeConfig {
  architecture: TargetArch;
  outputType: OutputType;
  frameworkVersion: string;
  iconPath?: string; // Optional path reference
  enableOptimization: boolean;
  allowUnsafe: boolean;
}

export interface BuildContext {
  metadata: ExeMetadata;
  config: ExeConfig;
  sourceCode: string; // The raw C# source
  buildLogs: string[];
}

